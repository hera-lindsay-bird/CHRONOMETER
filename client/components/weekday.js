export function weekday(weekday) {
  if (weekday === 1) {
    return '💧'
  } else if (weekday === 2) {
    return '🌿'
  } else if (weekday === 3) {
    return '💨'
  } else if (weekday === 4) {
    return '🔥'
  } else if (weekday === 5) {
    return '🪨'
  } else if (weekday === 6) {
    return '🦴'
  } else if (weekday === 0) {
    return '⏳'
  }
}
